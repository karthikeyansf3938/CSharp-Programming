namespace MovieTicketBooking
{
    /// <summary>
    ///  <see cref = "PersonalDetails"/>Get the personal information
    /// </summary>
    public class PersonalDetails
    {
        /// <summary>
        ///  <see cref = "Name"/>Get the name from the user.
        /// </summary>
        /// <value></value>
        public string Name{get; set;}
        /// <summary>
        ///  <see cref = "Age"/>Get the age of the user.
        /// </summary>
        /// <value></value>
        public int Age{get; set;}
        /// <summary>
        ///  <see cref = "PhoneNumber"/>Get the phone number of the user.
        /// </summary>
        /// <value></value>
        public long PhoneNumber{get; set;}
        
    }
}